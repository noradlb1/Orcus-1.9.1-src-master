namespace Orcus.Administration.Plugins.AudioPlugin
{
    /// <summary>
    ///     The genre of the audio
    /// </summary>
    public enum AudioGenre
    {
        /// <summary>
        ///     Troll the user e. g. a fly
        /// </summary>
        Troll,

        /// <summary>
        ///     Scare the user usual sounds from horror games e. g. the sound when the slender man appears
        /// </summary>
        Fear,

        /// <summary>
        ///     Somebody says something e. g. a voice message
        /// </summary>
        Voice,

        /// <summary>
        ///     You should be able to dance to it
        /// </summary>
        Music,

        /// <summary>
        ///     A sound taken from an application e. g. the skype message sound
        /// </summary>
        Application
    }
}